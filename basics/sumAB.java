
import java.util.*;

public class sumAB {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int a = in.nextInt(); // 1 will be stored in a
        int b = in.nextInt(); // 2 will be stored in b
        int sum = a + b; //sum of a and b will be stored in variable sum
        System.out.println(sum);
    }
}
