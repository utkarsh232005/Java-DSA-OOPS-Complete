
public class multiply {

    public static int multiplyNum(int a, int b) {
        int product = a * b;
        return product;
    }

    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        int prod = multiplyNum(a, b);
        System.out.println("the ans is: " + prod);

    }
}
